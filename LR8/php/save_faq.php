<?php
/**
 * ЗАДАНИЕ №2: Сохранение вопросов-ответов в файл
 * Цель: Сохранять данные из формы в текстовый файл
 */

// 1. СОЗДАНИЕ ПАПКИ ДЛЯ ФАЙЛОВ

// __DIR__ - путь к текущей папке (php/)
// /data - добавляем папку data
// Получаем: php/data/
$dataDir = __DIR__ . '/data';

// is_dir() - проверяет, существует ли папка
// Если папки нет - создаём её
if (!is_dir($dataDir)) {
    // mkdir() - создаёт папку
    // 0777 - права доступа (читать и писать могут все)
    // true - создавать вложенные папки, если их нет
    mkdir($dataDir, 0777, true);
}

// Полный путь к файлу, куда будем сохранять вопросы
// Например: php/data/faq.txt
$filename = $dataDir . '/faq.txt';


// 2. ПРОВЕРКА: ЭТО POST-ЗАПРОС?

// $_SERVER['REQUEST_METHOD'] - определяет метод запроса
// 'POST' - значит данные отправлены через форму
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    
    
    // 3. ПОЛУЧАЕМ ДАННЫЕ ИЗ ФОРМЫ
    
    // trim() - убирает лишние пробелы в начале и конце
    // $_POST['question'] - получаем значение поля "question" из формы
    // ?? '' - если поля нет, то используем пустую строку
    $question = trim($_POST['question'] ?? '');
    $answer = trim($_POST['answer'] ?? '');
    
    
    // 4. ПОДГОТАВЛИВАЕМ ОТВЕТ (JSON)
    
    // $response - массив, который потом превратится в JSON
    // success: false - пока считаем, что всё плохо
    // message: '' - пустое сообщение об ошибке
    $response = ['success' => false, 'message' => ''];
    
    
    // 5. ВАЛИДАЦИЯ (ПРОВЕРКА ДАННЫХ)
    
    // empty() - проверяет, пустая ли переменная
    // Если вопрос пустой - выдаём ошибку
    if (empty($question)) {
        $response['message'] = 'Введите вопрос';
        
    // Если ответ пустой - выдаём ошибку
    } elseif (empty($answer)) {
        $response['message'] = 'Введите ответ';
        
    // Если всё заполнено - сохраняем в файл
    } else {
        
        
        // 6. СОХРАНЕНИЕ В ФАЙЛ
        
        // date() - текущая дата и время
        // 'Y-m-d H:i:s' - формат: 2024-01-15 14:30:25
        $date = date('Y-m-d H:i:s');
        
        // Формируем запись для файла
        // \n - символ переноса строки
        // --- - разделитель между разными вопросами
        $record = "Вопрос: $question\nОтвет: $answer\nДата: $date\n---\n";
        
        
        // fopen() - открывает файл
        // "a" - режим "добавление в конец" (append)
        // Если файла нет - создаётся новый
        // Если файл есть - новые данные дописываются в конец
        $f = fopen($filename, 'a');
        
        // Проверяем, что файл открылся успешно
        if ($f) {
            
            // fwrite() - записывает данные в файл
            fwrite($f, $record);
            
            // fclose() - ОБЯЗАТЕЛЬНО закрываем файл!
            fclose($f);
            
            // Говорим, что всё успешно
            $response['success'] = true;
            $response['message'] = 'Вопрос добавлен!';
            
        } else {
            // Если не удалось открыть файл - ошибка
            $response['message'] = 'Ошибка сервера';
        }
    }
    
    
    // 7. ОТПРАВКА ОТВЕТА (В ФОРМАТЕ JSON)
    
    // header() - устанавливает заголовок ответа
    // Content-Type: application/json - говорит, что мы отправляем JSON
    header('Content-Type: application/json');
    
    // json_encode() - превращает массив в JSON-строку
    // JSON_UNESCAPED_UNICODE - не превращать русские буквы в \u0442\u0435\u043a\u0441\u0442
    echo json_encode($response, JSON_UNESCAPED_UNICODE);
    
    // exit - завершаем выполнение скрипта
    exit;
}


// 8. ЕСЛИ НЕ POST-ЗАПРОС

// Если кто-то просто открыл этот файл в браузере (не через форму)
// или отправил GET-запрос - выдаём ошибку
header('Content-Type: application/json');
echo json_encode(['success' => false, 'message' => 'Неверный метод'], JSON_UNESCAPED_UNICODE);
exit;
?>