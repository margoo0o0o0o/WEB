<?php
// === ЗАДАНИЕ №2 (Начало работы с сессиями) ===
// Запускаем сессию, чтобы PHP мог сохранять данные между страницами
session_start();

// === ЗАДАНИЕ №1 (Методы передачи данных) ===
// Проверяем, что запрос был отправлен методом POST
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    
    // Получаем данные из формы и сохраняем их в переменные
    // Используем htmlspecialchars для безопасности (защита от XSS)
    $name = htmlspecialchars($_POST['name']);
    $gender = $_POST['gender'] ?? 'Не указан';
    
    // === ЗАДАНИЕ №2.1 (Создание сессионных переменных) ===
    // Записываем полученные данные в суперглобальный массив $_SESSION
    // Теперь эти данные будут доступны на любом другом файле сайта
    $_SESSION['name'] = $name;
    $_SESSION['gender'] = $gender;
    
    // === ЗАДАНИЕ №2.2 (Переход на вторую страницу) ===
    // Используем заголовок для автоматического перенаправления на page2.php
    header("Location: page2.php");
    
    // Всегда завершаем скрипт после команды перенаправления
    exit();
}
?>