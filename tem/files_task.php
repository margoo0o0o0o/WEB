<?php
// === ЗАДАНИЕ №3 (Работа с файлами) ===

$filename = "1.txt";

// 1. Запись 5 произвольных строк в файл
// Используем массив строк и функцию implode для объединения их с переносом строки
$data = "Строка 1: Привет!\nСтрока 2: Это PHP.\nСтрока 3: Изучаем файлы.\nСтрока 4: Все работает.\nСтрока 5: Конец списка.";

// Записываем данные в файл. Если файл существует, он будет перезаписан.
file_put_contents($filename, $data);

echo "<h3>Файл $filename успешно создан и заполнен.</h3>";

// 2. Чтение первых 2 строк из файла
echo "<h3>Первые две строки из файла:</h3>";

// Открываем файл для чтения
$file = fopen($filename, "r");

if ($file) {
    // Считываем первую строку
    echo fgets($file) . "<br>";
    // Считываем вторую строку
    echo fgets($file) . "<br>";
    
    // Обязательно закрываем файл для освобождения ресурсов
    fclose($file);
} else {
    echo "Не удалось открыть файл.";
}
?>