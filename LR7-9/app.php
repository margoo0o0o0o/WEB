 * Все запросы проходят через этот файл
 * Пример: app.php?page=catalog
 */

// ============================================================
// АВТОЗАГРУЗКА КЛАССОВ
// ============================================================
// spl_autoload_register — автоматически подгружает классы
// когда они вызываются. Не нужно писать require для каждого файла.
// ============================================================
spl_autoload_register(function ($class) {
    // Преобразуем namespace в путь к файлу
    // Например: App\Controllers\UniversityController → app/controllers/UniversityController.php
    $file = __DIR__ . '/' . str_replace('\\', '/', $class) . '.php';
    if (file_exists($file)) {
        require_once $file;
    }
});

// ============================================================
// ПОДКЛЮЧАЕМ ЯДРО
// ============================================================
require_once 'core/Router.php';
require_once 'core/Controller.php';
require_once 'core/Model.php';

// ============================================================
// ОПРЕДЕЛЯЕМ, КАКУЮ СТРАНИЦУ ЗАПРОСИЛ ПОЛЬЗОВАТЕЛЬ
// ============================================================
// Если параметр page не передан — показываем каталог
$page = isset($_GET['page']) ? $_GET['page'] : 'catalog';

// ============================================================
// СОЗДАЁМ МАРШРУТЫ
// ============================================================
$router = new Core\Router();

// Формат: add('название_страницы', 'Контроллер', 'метод')
$router->add('catalog', 'UniversityController', 'catalog');  // Каталог
$router->add('cart', 'CartController', 'index');              // Корзина
$router->add('cart_add', 'CartController', 'add');            // Добавить в корзину
$router->add('cart_remove', 'CartController', 'remove');      // Удалить из корзины
$router->add('cart_clear', 'CartController', 'clear');        // Очистить корзину
$router->add('cart_order', 'CartController', 'order');        // Оформить заказ

// ============================================================
// ЗАПУСКАЕМ МАРШРУТИЗАЦИЮ
// ============================================================
$router->dispatch($page);с