<?php
// ЗАПУСКАЕМ СЕССИЮ. Это обязательно делать в самом начале!
session_start();

// ОПРЕДЕЛЯЕМ, КАКУЮ СТРАНИЦУ ПОКАЗЫВАТЬ
// Если в адресной строке есть ?page=2, то показываем страницу 2
// Если ничего нет, показываем страницу 1
$page = isset($_GET['page']) ? $_GET['page'] : 1;

// =============================================
// СТРАНИЦА 1 - СОЗДАЕМ СЕССИЮ
// =============================================
if ($page == 1) {
    // Сохраняем имя пользователя в сессию
    $_SESSION['username'] = "maksim";
    
    // Выводим HTML
    ?>
    <h3>СТРАНИЦА 1 - СОЗДАНИЕ СЕССИИ</h3>
    <p>Привет, <?php echo $_SESSION['username']; ?></p>
    <p>Переменная username сохранена в сессии</p>
    
    <!-- Ссылка на страницу 2 (в адресной строке появится ?page=2) -->
    <a href="?page=2">Перейти на страницу 2</a>
    <?php
}

// =============================================
// СТРАНИЦА 2 - ЧИТАЕМ СЕССИЮ
// =============================================
elseif ($page == 2) {
    ?>
    <h3>СТРАНИЦА 2 - ЧТЕНИЕ СЕССИИ</h3>
    
    <!-- Читаем имя из сессии и выводим -->
    <p><?php echo $_SESSION['username']; ?>, ты пришел на другую страницу этого сайта!</p>
    
    <!-- Ссылка на страницу 3 -->
    <a href="?page=3">Перейти на страницу 3</a>
    <?php
}

// =============================================
// СТРАНИЦА 3 - УДАЛЯЕМ СЕССИЮ
// =============================================
elseif ($page == 3) {
    // Удаляем переменную username из сессии
    unset($_SESSION['username']);
    
    ?>
    <h3>СТРАНИЦА 3 - УДАЛЕНИЕ СЕССИИ</h3>
    
    <!-- Теперь имя пустое, так как мы его удалили -->
    <p>Привет, <?php echo $_SESSION['username']; ?></p>
    <p>Переменная username удалена! (она пустая)</p>
    
    <!-- Полностью уничтожаем сессию -->
    <?php session_destroy(); ?>
    
    <!-- Ссылка на страницу 1, чтобы начать заново -->
    <a href="?page=1">Начать сначала</a>
    <?php
}

// ПОКАЗЫВАЕМ ID СЕССИИ (для наглядности)
echo "<hr>";
echo "ID вашей сессии: " . session_id();
?>